# Usamos o for pra repetir uma ação várias vezes sem escrever o mesmo código várias vezes.
# O i representa o número da repetição atual, e pode ser usado pra mostrar ou calcular algo diferente em cada volta.
# Nesse caso, estamos só imprimindo o número da vez, de 0 até 10.
for i in range(11):
    print(i)
