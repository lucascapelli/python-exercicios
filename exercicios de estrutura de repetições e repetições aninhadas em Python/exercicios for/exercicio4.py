# Mostrar a contagem regressiva de 10 até 1

for i in range(10, 0, -1):
    print(i)

# Usamos o range com três argumentos: início, fim e passo.
# Começamos em 10, vamos até 1 (por isso colocamos 0 como limite, porque ele para antes).
# O -1 indica que estamos diminuindo de 1 em 1 (contagem regressiva).
